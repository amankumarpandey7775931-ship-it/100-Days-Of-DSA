/*Problem: Given a sorted array of n integers, remove duplicates in-place. Print only unique elements in order.

*/
#include <stdio.h>

int main() {
    int n;
    scanf("%d", &n);

    if (n == 0) return 0;

    int arr[n];
    for (int i = 0; i < n; i++) {
        scanf("%d", &arr[i]);
    }

    int index = 0;

    for (int i = 1; i < n; i++) {
        if (arr[i] != arr[index]) {
            index++;
            arr[index] = arr[i];
        }
    }

    for (int i = 0; i <= index; i++) {
        printf("%d ", arr[i]);
    }

    return 0;
}
